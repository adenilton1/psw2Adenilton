/**
 *
 * @author RA21505955
 */
@Entity

public class Produto {
    private float preco;

    private String nome;
@Id
@GeneratedValue(strategy = GenerationType.IDENTITY)
    private int identificador;
    public float getPreco() {
        return preco;
    }

    public void setPreco(float preco) {
        this.preco = preco;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }
    
}
